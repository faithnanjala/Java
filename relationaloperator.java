class relationaloperator 
{
    public static void main(String[] args) 
    {
        //<,>,==,!=,<=,>=
        int a = 100;
        int b = 120;
        boolean result = a ==b;
        boolean result1 = a >= b;
        boolean result3 = a <= b;
        System.out.println(result);
        System.out.println(result1);
        System.out.println(result3);
    }

}
